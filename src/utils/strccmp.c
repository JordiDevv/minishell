#include "../../inc/executor.h"

int strccmp(char *s1, char *s2, char c)
{
    int i;

    i = 0;
    while (s1[i] && s2[i])
    {
        if (s1[i] == c && s2[i] == c)
            return (0);
        i++;
    }
    return (1);
}
